class Solution:
    def getIntersectionNode(self, headA: ListNode, headB: ListNode) -> Optional[ListNode]:
        if not headA or not headB:
            return None 

        pA, pB = headA, headB

        while pA != pB: 
            pA = headB if pA is None else pA.next 
            pB = headA if pB is None else pB.next 

        return pA 